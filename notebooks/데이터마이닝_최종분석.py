#    법정동과 행정동 이름 차이를 완화하기 위한 표준화
# ------------------------------------------------------------

def normalize_text(x):
    if pd.isna(x):
        return ""
    x = str(x)
    x = re.sub(r"\s+", "", x)
    return x.strip()


def make_dong_key(dong):
    """
    법정동·행정동 이름을 넓은 기준으로 표준화한다.

    예:
    - 상도동, 상도제1동, 상도1동 → 상도
    - 성수동1가, 성수1가제1동 → 성수
    - 금호동4가, 금호4가동 → 금호
    - 종로1·2·3·4가동 → 종로
    """
    x = normalize_text(dong)

    if x == "":
        return ""

    # 괄호 제거
    x = re.sub(r"\([^)]*\)", "", x)

    # 가운데점, 마침표 등 제거
    x = x.replace("·", "")
    x = x.replace(".", "")

    # '성수동1가' → '성수1가', '금호동4가' → '금호4가'
    x = re.sub(r"([가-힣]+)동(\d+)가", r"\1\2가", x)

    # '성수1가제1동' → '성수1가'
    x = re.sub(r"제\d+동$", "", x)

    # '상도1동', '역삼2동' → '상도', '역삼'
    x = re.sub(r"\d+동$", "", x)

    # '면목본동' → '면목'
    x = re.sub(r"본동$", "", x)

    # '금호1가동' → '금호1가'
    x = re.sub(r"(\d+)가동$", r"\1가", x)

    # '성수1가', '금호4가', '종로1234가' → '성수', '금호', '종로'
    x = re.sub(r"\d+가$", "", x)

    # 마지막 '동' 제거: '역삼동' → '역삼'
    x = re.sub(r"동$", "", x)

    return x


