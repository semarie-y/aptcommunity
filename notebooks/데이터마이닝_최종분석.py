def mean_compare_by_target(data, feature, target):
    no_mean = data.loc[data[target] == 0, feature].mean()
    yes_mean = data.loc[data[target] == 1, feature].mean()
    diff = yes_mean - no_mean
    corr = data[[feature, target]].corr().iloc[0, 1]

